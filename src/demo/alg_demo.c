// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
//                 buffer_init
// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
// Description:
//    buffer initialize
//
// Input:
//    addr, buffer address
//    len, buffer length
//    type, initialize type, should be MEM_SEQUENCE, MEM_RANDOM or MEM_FIX
//    pattern, fixed pattern, for type of MEM_FIX
//
// Output:
//    None.
//
// ~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
void buffer_init(UINT32 addr, UINT32 len, MEM_INIT_TYPE type, UINT8 pattern)
{
	UINT32 i;
	UINT8* p_buf;

	p_buf = (UINT8*)addr;
	for(i = 0; i < len; i++)
	{
	switch(type)
	{
		case MEM_SEQUENCE:
			*p_buf = i;
			break;
		case MEM_RANDOM:
			*p_buf = trng_rand();
			break;
		case MEM_FIX:
			*p_buf = pattern;
			break;
		}
		p_buf++;
	}
}

